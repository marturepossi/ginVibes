          <h2 class="text-center">Nuestra empresa</h2>
          <p>
          <strong>Gin Vibes</strong> se destila en el corazón de Argentina, donde la tradición de la artesanía y la <em>pasión</em> por la ginebra convergen en un destilado de exquisita calidad y sabor inigualable. Fundada en los fértiles valles de Mendoza, conocidos por sus viñedos de renombre mundial, nuestra destilería ha heredado el legado de la meticulosa selección de ingredientes y la precisión en el arte de la destilación.
          </p>
          <p>
          Con cada botella, se celebra la fusión de las hierbas autóctonas y los botánicos seleccionados a mano, los cuales se combinan con el agua pura de los Andes para crear una ginebra única que es tanto un tributo a la tierra como un regalo para el paladar. <strong>Gin Vibes</strong> no es solo una marca, es una <em>experiencia sensorial</em> que invita a los conocedores a un viaje por los sabores y aromas más auténticos de Argentina.
          </p>
      </div>
      
      <div class="col-md-12 col-xl-6">
          <h2 class="text-center">Nuestra filosofía</h2>
          <p>
          La filosofía de <strong>Gin Vibes</strong> se basa en la <em>armonía</em> con la naturaleza y el compromiso con la sostenibilidad. Desde sus inicios, la empresa ha adoptado prácticas ecológicas y un enfoque de comercio justo que respalda a las comunidades locales y protege el medio ambiente. <strong>Gin Vibes</strong> se enorgullece de ser pionera en técnicas de destilación innovadoras que no solo preservan la pureza del gin sino que también reducen la huella de carbono.
          </p>
          <p>
          Nuestro objetivo es llevar el <em>espíritu de Argentina</em> al mundo, compartiendo un gin que no solo es apreciado por su sabor superior, sino también por la historia y los valores que representa. Cada sorbo de <strong>Gin Vibes</strong> es un brindis por un futuro más verde y una invitación a vivir la vida con <em>pasión, alegría</em> y un espíritu indomable.
          </p>
